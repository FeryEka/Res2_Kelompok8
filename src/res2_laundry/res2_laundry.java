    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Data Anda Akan Dirubah?", "Konfirmasi Update", JOptionPane.YES_NO_OPTION);
        try {
            sql = "update laundry set Tanggal=?,KdPelanggan=?,Nama_Pelanggan=?,NoTelp=?,"
                    + "Alamat=?,BeratPakaian=?,Harga=?,TotalBayar=? where KdPelanggan='" + jTextkode.getText() + "'";
            PreparedStatement edit = con.prepareStatement(sql);
            if (ok == 0) {
                try {

                    edit.setString(1, jTexttgl.getText());
                    edit.setString(2, jTextkode.getText());
                    edit.setString(3, jTextnama.getText());
                    edit.setString(4, jTexttelp.getText());
                    edit.setString(5, jTextalamat.getText());
                    edit.setString(6, jTextberat.getText());
                    edit.setString(7, jTextharga.getText());
                    edit.setString(8, jTexttotal.getText());

                    edit.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Update Data Berhasil");

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Update Data Tidak Berhasil");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "TABEL SALAH");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

