    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        try {
            Object[] rows = {"NO", "TANGGAL", "KODE PELANGGAN", "NAMA PELANGGAN", "NO TELP", "ALAMAT", "BERAT PAKAIAN", "HARGA", "TOTAL BAYAR"};

            dtm = new DefaultTableModel(null, rows);
            jTable1.setModel(dtm);
            jTable1.setBorder(null);
            jScrollPane2.setVisible(true);
            jScrollPane2.setViewportView(jTable1);
            int a = 1;
            String tgl = "", kode = "", nama = "", no = "", alamat = "", berat = "", harga = "", bayar = "";
            try {
                String sql = "SELECT * FROM laundry";
                st = con.createStatement();
                rs = st.executeQuery(sql);
                while (rs.next()) {
                    tgl = rs.getString("Tanggal");
                    kode = rs.getString("KdPelanggan");
                    nama = rs.getString("Nama_Pelanggan");
                    no = rs.getString("NoTelp");
                    alamat = rs.getString("Alamat");
                    berat = rs.getString("BeratPakaian");
                    harga = rs.getString("Harga");
                    bayar = rs.getString("TotalBayar");

                    String[] tampil = {"" + a++, tgl, kode, nama, no, alamat, berat, harga, bayar};
                    dtm.addRow(tampil);

                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Query Salah" + e);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

